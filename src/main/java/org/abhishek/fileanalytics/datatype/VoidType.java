package org.abhishek.fileanalytics.datatype;

/**
 * Data type to represent VOID return. This is created to conform to the
 * Generics approach in Java.
 * 
 * @author abhishek
 * @since 1.0
 */
public class VoidType {

    /**
     * Prints the class name.
     * 
     * @author abhishek
     * @since 1.0
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        return VoidType.class.toString();
    }

}
